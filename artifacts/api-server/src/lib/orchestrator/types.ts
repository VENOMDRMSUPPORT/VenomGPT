/**
 * orchestrator/types.ts -- Shared types for the VenomGPT orchestration layer.
 *
 * Defines the core structures used by the task router, planner, action router,
 * and run-state machine that together replace the previous single-loop approach.
 *
 * =====================================================================
 * RunState Type Overview
 * =====================================================================
 *
 * RunState is the central mutable execution state object for a single agent
 * task run. It is created by createRunState() at the start of every task and
 * updated by the action router after each step in the agent loop.
 *
 * Lifecycle:
 *   1. createRunState() initialises all fields to safe defaults.
 *   2. The agent loop advances step and transitions phase through:
 *        initializing -> [planning] -> executing -> [verifying|repairing] -> complete|failed
 *   3. The action router mutates tracking fields (filesRead, filesWritten,
 *      commandsRun, gateCounts, etc.) after every action.
 *   4. On completion the state is consumed for execution-summary telemetry.
 *
 * Field groups:
 *
 *   Phase and step control
 *     phase        - Current RunPhase in the state machine (see RunPhase union).
 *     step         - Monotonic counter incremented each loop iteration.
 *     maxSteps     - Effective ceiling (min of operator setting and profile cap).
 *     profile      - The ExecutionProfile governing resource limits and behaviour.
 *     plan         - Optional ExecutionPlan produced by the planning phase.
 *
 *   Resource tracking (enforced by gate checks in actionRouter)
 *     filesRead    - Set of distinct file paths read this session.
 *     filesWritten - Set of distinct file paths written this session.
 *     commandsRun  - Ordered list of all shell commands executed.
 *
 *   Failure and repair tracking
 *     lastActionType         - Type string of the most recent action (or null).
 *     lastActionFailed       - Whether the last action returned an error.
 *     consecutiveFailures    - Count of sequential failures (triggers repair nudge).
 *     consecutiveParseFailures - Count of sequential JSON/model parse failures.
 *
 *   Verification subsystem
 *     unverifiedWrites  - Files written since the last successful verification.
 *     verificationsDone - Total successful verification steps completed.
 *
 *   Telemetry and diagnostics
 *     shellReadsBlocked - Shell commands blocked as file-read bypass attempts.
 *     gateCounts        - Partial map of GateRejectionReason to count, used for
 *                         execution summary and debugging.
 *
 *   Runtime lifecycle (task-6 pass)
 *     sideEffectsObserved  - Classified side-effect entry per run_command.
 *     verificationLedger   - Evidence accumulator with quality ladder.
 *     runtimeSnapshots     - Before/after port-state snapshots for server commands.
 *     verificationQuality  - Ledger quality label, mirrored for fast gate checks.
 *
 * =====================================================================
 */

