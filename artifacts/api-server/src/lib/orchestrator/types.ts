// --- Run phase state machine ---------------------------------------------------

/**
 * Explicit phase tracking for task execution.
 * Transitions: initializing -> [planning] -> executing -> [verifying|repairing] -> complete|failed
 *
 * task-9 additions:
 *   awaiting_approval  — halted at an approval gate; operator must act before the loop continues.
 *   selectively_blocked — some lanes were not approved in a selective decision; those lanes are held.
 *   approval_denied    — gate was explicitly denied; run has halted with this terminal/non-terminal state.
 *   operator_overridden — a step was skipped/denied/substituted by an operator override.
 */
export type RunPhase =
  | "initializing"
  | "planning"
  | "inspecting"
  | "executing"
  | "verifying"
  | "repairing"
  | "wrapping_up"
  | "complete"
  | "blocked"
  | "failed"
  | "awaiting_approval"
  | "selectively_blocked"
  | "approval_denied"
  | "operator_overridden";

// --- Gate rejection reason (must match actionRouter.ts) ------------------------

export type GateRejectionReason =
  | "shell_read_redundant"
  | "shell_read_cap_exceeded"
  | "redundant_read"
  | "read_cap_exceeded"
  | "write_cap_exceeded"
  | "write_class_blocked"
  | "command_cap_exceeded"
  | "post_verify_read_blocked"
  | "verification_required"
  | "runtime_proof_required"
  /** Blocked because the file was already written+confirmed in the prior continuation run. */
  | "continuation_step_already_completed";

