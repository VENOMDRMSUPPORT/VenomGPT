// --- Continuation chain types (task-8 pass) -----------------------------------

/**
 * A single step entry in the what-remains model.
 * Derived from the original plan vs. completed action history.
 */
export interface WhatRemainsStep {
  /** Step identifier derived from plan expectedChanges index or file path. */
  id: string;
  /** Human-readable label for this step. */
  label: string;
  /** Status of this step at the time of resume. */
  status: "completed" | "remaining" | "failed" | "blocked";
  /** Reason this step is failed/blocked, if applicable. */
  reason?: string;
  /**
   * Structured file path this step operates on (when the step is a file write).
   * Used for deterministic gate enforcement — do NOT derive from label string parsing.
   * Absent for non-file steps (e.g. agent "remaining work" text entries).
   */
  filePath?: string;
}

/**
 * The structured what-remains model produced at resume time.
 * Derived from the original plan steps minus confirmed-complete action history.
 * Never generated by narrative heuristics — always grounded in real prior state.
 */
export interface WhatRemains {
  /** Steps already confirmed complete (from action history). */
  completedSteps: WhatRemainsStep[];
  /** Steps still outstanding (from plan minus completed). */
  remainingSteps: WhatRemainsStep[];
  /** Steps that were attempted but failed or blocked. */
  failedSteps: WhatRemainsStep[];
  /** Source checkpoint ID and timestamp for auditability. */
  groundedFrom: {
    checkpointId: string;
    checkpointCreatedAt: string;
  };
}

/**
 * Continuation lineage embedded in API responses.
 * Populated only when the task is a genuine resumed continuation — never fabricated.
 */
export interface ContinuationLineage {
  /** True when this task is a structured continuation of a prior run. */
  isContinuation: true;
  /** Task ID of the run this continues from. */
  parentTaskId: string;
  /** Checkpoint ID that grounded this continuation. */
  originCheckpointId: string;
  /** Number of continuation hops from the original run (1 = first continuation). */
  ancestryDepth: number;
  /** The what-remains snapshot computed at resume time. */
  whatRemainedAtResume: WhatRemains;
}

/**
 * Full continuation chain record stored on a resumed RunState.
 * Carries all fields needed to identify, audit, and surface continuation truth.
 */
export interface ContinuationChain {
  /** Task ID of the immediate parent run. */
  parentTaskId: string;
  /** Checkpoint that grounded this continuation. */
  originCheckpointId: string;
  /** True when this is a structured continuation (vs. an unstructured retry). */
  isStructuredContinuation: true;
  /** How many continuation hops from the first run (1 = first continuation). */
  ancestryDepth: number;
  /** What-remains snapshot built at resume time from real prior state. */
  whatRemainedAtResume: WhatRemains;
  /** ISO timestamp of when the continuation was created. */
  resumedAt: string;
}

